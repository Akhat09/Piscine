void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int i;

	i = 1;
	if (nb < 0)
	{
		ft_putchar('-');
		i = -1;
	}
	if (nb / 10 != 0)
		ft_putnbr(nb / 10 * i);
	ft_putchar(nb % 10 * i + '0');
}
