int	ft_is_prime(int nb)
{
	int i;

	if (nb == 2 || nb % 2 == 0 || nb <= 1)
		return (nb == 2 ? 1 : 0);
	i = nb / 2 + 1;
	while (--i > 1)
		if (nb % i == 0)
			return (0);
	return (1);
}
