#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}
void	ft_putnbr(int nb)
{
	int i;

	i = nb;
	if (i < 0)
	{
		ft_putchar('-');
		i = i * (-1);
	}
	if (i >= 10)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	else
	{
		ft_putchar(i + '0');
	}
}

int		main(void)
{
	ft_putnbr(10);
	ft_putnbr(-10);
	ft_putnbr(3213);
	ft_putnbr(-3213);
	ft_putnbr(0);
}
